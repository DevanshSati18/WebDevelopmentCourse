// For loop 
// for ( initial ; condition ; increment){}

console.log("for loop");

for( a=0 ; a<10; a++){
    console.log(a);
}


// while loop
// while(condition)
let b = 0;
console.log("while loop");
 while (b<10) {

    console.log(b);
    b++;
 }


//do while loop


let i=0;

do{
    console.log(i);
    i++;
}while(i<10);